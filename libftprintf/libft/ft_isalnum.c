int		ft_isalnum(int c)
{
	int		i;

	i = 'A';
	while (i <= 'z')
	{
		if (i == c)
			return (1);
		if (i == 'Z')
			i = 'a' - 1;
		i++;
	}
	i = '0';
	while (i <= '9')
	{
		if (i == c)
			return (1);
		i++;
	}
	return (0);
}
